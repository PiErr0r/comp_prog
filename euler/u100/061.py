hexagonal = lambda n: n*(2*n-1)
heptagonal = lambda n: n*(5*n-3)//2
octagonal = lambda n: n*(3*n-2)

digits = [
	find4digit(triangle),
	find4digit(square),
	find4digit(pentagonal),
	find4digit(hexagonal),
	find4digit(heptagonal),
	find4digit(octagonal),
]


